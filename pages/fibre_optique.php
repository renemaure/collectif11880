					<p class="justify">
						Le département de l’Yonne déploie depuis un bon moment la fibre optique sur les communes. Sur la communauté de commune de Migennes, vous pouvez dès à présent savoir si votre domicile ou bâtiment est déjà pré-raccordé, en passe de l’être ou la date de raccordement en entrant votre adresse dans la carte à droite.	
					</p>
					<p class="justify">
						Si vous êtes pré-raccordé vous devez vous rapprocher du fournisseur de votre choix, mais les fournisseurs d’accès ne se précipitent pas pour le moment il n’y a que Orange comme choix, vive la concurrence.
					</p>
					<p class="justify">
						Le département de l’Yonne stipule sur son site : « Le réseau construit par votre Département est ouvert et neutre, de sorte que tout Fournisseur d’Accès Internet (national, local ou régional) peut, s’il le souhaite, proposer la fibre sur les territoires couverts. »
					</p>
					<p class="justify">
						L’association vous tiendra au courant des nouvelles. Pour nous les questions sont : l’abonné a-t-il une IP fixe ou mobile ; faut-il une DMZ ou une autre protection en sortie de fibre ; est que la connexion a la même capacité en montée et en décente ; et encore plein d’autres questions !
