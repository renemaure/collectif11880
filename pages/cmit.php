          <h2 class="coul-bord text-center">Club CMIT collection d'ordinateurs</h2>
          <p class="text-justify">
            D’après le texte de l’exposition L’histoire de l’informatique qui a eu lieu en juillet 1999 au FJT de Sens.
          </p>
         <p class="text-justify">
           Oui, pourquoi collectionner des ordinateurs, surtout des ordinosaures. Justement parce que l’informatique court à une vitesse folle après la technologie, que beaucoup sont persuadés de tout savoir sur ces même technologies, parce que nous avons oublié ce qu’était l’informatique d’hier, parce que nous avons à peine acheté le soi-disant dernier cri informatique qu’il est déjà obsolète, que votre meilleur ami ou amie se moque de vous, mais payable en 24 mois. Sans parler de votre ami ou amie, le ou la même d’ailleurs, vous vante les mérites d’internet, mais ne sachant pas ce qu’est un routeur, ou essaye de vous placer le dernier tube à la mode du groupe MMX.
        </p>
        <p class="text-justify">
          Oui, l’informatique va trop vite, il nous faut une petite pose pour faire le point et flâner parmi ses joyeuses machines tout droit sorti d’une série B des années 70. Voilà pourquoi je collectionne les ordinateurs, aussi pour que plus tard cela ne ressemble pas à une légende du genre X files et peut être, en étant largement pompeux, contribuer à faire l’histoire de l’informatique.
        </p>
