      <p class="g f12 s">Construction de la fourche arrière&nbsp;:</p>
      <p class="jst">
      <span class="g">1):</span> Il faut découper 4 barres dans du tube d’acier de 30X30 millimètre et 2 pièces dans du méplat de 30mm.
      </p>
      <p class="cnt g ">
        2 tubes de 430MM<br>
        1 tube  de 250mm<br>
        1 tube de  150mm<br>
        2 méplats de 40mm<br>
        2 méplats de 25mm
      </p><p class="jst">
        Les deux premiers tubes de <span class="b i">430mm</span> forment les jambes de la fourche arrière, qui maintiennent la roue au sommet du tube de selle. Cette longueur est un peu plus longue que la cote finale, pour que les angles puissent ètre dérivés au fur et à mesure de la fabrication.
        </p><p class="ph10">
        Le tube de <span class="b i">250mm</span> sert à au montage à blanc de la fourche.
        <br>
        Le tube de <span class="b i">150mm</span> sert traverse pour la fourche arrière.
        <br>
        Les méplats de <span class="b i">30mm</span> ferment les bouts des fourches.
        <br>
        Les méplats de <span class="b i">25mm</span> ferment les sections de La traverse de fourches.
      </p><p class="jst ph10">
      <span class="g">2):</span> A l’une des extrémités de chaque tube de 430mm (figure 2), on trace une coupe à <span class="g i"> 45 degrés</span>, donnant aux fourches arrière un aspect plus épuré.
