    </div>
    <div class="shadow p-4 mb-3 bg-light rounded"> 
      <h4 class="coul-bord cnt g">Introduction</h4>
      <p class="just">
        Le vélo Warrior est construit à partir de composants de vélo de récupération et de tubes en acier. Il ne possède pas de pièces usinées.Il est construit en utilisant uniquement un outillage accessible à un bricoleur.
      </p>
      <p class="just">
        Les vélos couchés surbaissés ne sont pas seulement confortables mais donnent également au cycliste un avantage aérodynamique et permet de transmettre l’énergie humaine directement dans les pédales.
      </p>
      <p class="cnt coul-bord g">
        Risques pour la santé induits par la pratique de la soudure
      </p>
      <p class="just">
        Tous les procédés de soudage produisent des fumées et des gaz dans une plus ou moins grande mesure. Les aciers galvanisés produisent des vapeurs ajoutées par le revêtement de zinc vaporisé. Les fumées de soudage de l’acier galvanisé peuvent contenir du zinc, du fer et du plomb. Utilisez des protections, une bonne ventilation de l’air, des appareils respiratoires et des systèmes d’extraction des fumées, y compris des ventilateurs à grande vitesse avec filtres.
      </p>
      <p class="cnt coul-bord g">Note d'avant construction</p>

      <p class="just">
        On utilisera un mètre double lecture en pouces et en millimètre, car certaines cotes seront laissées en pouces pour rester au plus près de l'original.
      </p>
      <p class="cnt coul-bord g"> Unité de mesure :</p>
      <p>
        Comme dans toute construction métallurgique on utilise le millimètre et uniquement le millimètre, ou le pouce et uniquement le pouce !
      </p>
      <p class="just">
        Pour la construction de notre vélo on utilisera comme marbre de montage une table en bois de récupération mais droite dans les deux dimensions, ou presque.
      </p>
    </div>
