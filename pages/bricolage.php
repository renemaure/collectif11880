      <br>
      <span class="g">6):</span> On bloque le tube de montage sur la table marbre avec des serre-joints. On position les pointes des 2 jambes de fourche arrière sur les traits tracer à <span class="g">l’étape 5</span>, en faisant attention que leurs sommets se joignent exactement pour former un triangle comme le montre la <span class="g">photo N°6</span>.
