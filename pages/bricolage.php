      <span class="g">7):</span> Nous soudons par points la pointe de chaque jambe sur un des deux axes tracés sur le tube de montage. Les soudures par points fonctionnant comme des charnières et permettant d'écarter les jambes à leur position.
      <br>
        Avec ce montage on s’assure que les angles sont corrects sans même savoir ce qu'ils valent, plutôt que d'essayer de calculer ces angles subtils à partir d'un dessin et d'espérer qu'ils seront précis. Si, si, nous avons passé une soirée à essayer de calculer et de dessiner les angles de la fourche arrière !
      </p><p class="jst ph10">
      <span class="g">8):</span> on prolonge sur la table de marbre le centre tracer sur le tube de montage, puis on positionne les deux jambes de fourches de chaque côté avec un entre axe de <span class="g">2 pouces</span> cote mesurée à l’intérieur des tubes. En faisant cela nos soudures ont commencé à craquées, dû à utilisation d’une soudure semi-automatique alors on a préféré tout bloquer en position avec des vis directement visser dans la table ! <span class="g">photo N°7</span>.
