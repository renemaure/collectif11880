			<div class="boite12">
			<h4>Historique de l'association</h4>
				<p>
					Notre association a été créée le 22 janvier 1996 à Saint Martin du Tertre dans l’Yonne sous le nom de Conservatoire du matériel industriel et technique, ou CMIT. Après huit ans de fonctionnement dans l’Yonne et son expulsion du foyer des jeunes travailleurs de Sens notre association déménage à Paris dans le 13ème et en profite pour modifier son nom et devient le collectif 11880 club CMIT, pour plus de 14 ans de fonctionnement et pas mal d’aventures.
				</p>
				<p>
					Après un sommeil de 3ans, elle revient dans l’Yonne début 2021 et s’installe à Migennes au 11, place de la République et commence à reprendre ses activités avec une permanence tous les dimanches matin de 9 à 12 heures pour y bidouiller de la robotique et de la programmation.
				<p>
					Premier chantier pour cette année 2021 consiste à refaire entièrement un notre site internet qui en est déjà à sa à sa sixième mouture, mais aussi qu’un nouveau projet de robotique avec la création d’un assistant trop personnel. Le collectif continu aussi les projets Matricule et erreur Système, ainsi qu’un nouveau projet de diffusion d’art libre nommé ProjetX.			
				</p>
