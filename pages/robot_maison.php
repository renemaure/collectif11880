				<h3 class="blog-post-title">Mars 2017</h3>	
				<p>	
				Je découvre lors d’un reportage à la télé, des robots carrés travaillant en esclave chez Amazon, nommés Kiva. Ils sont de couleur orange et de forme carré. C’est le déclic ! Pourquoi faire comme tout le monde un robot au formes rondes. La preuve, Amazon en on construit un carré et joli en plus. Voilà l’idée, un robot cubique, pas trop grand ni trop haut, mais assez pour contenir tout le matériel que j’ai glané sur les vides greniers pendant ces 14 mois de réflexions, mais aussi les dons et les achats en solde. L’idée prend forme, je supprime au passage la fonction aspirateur trop gourmande en énergie et d’un rendement médiocre pour ne garder que le robot de compagnie mais à l’attitude d’un humain.
				</p><p>
				Pour rester dans l’idée du robot balais, je garde comme base principale le sous-pot à fleur, mais de forme carré de 28 centimètres de côté et 4 centimètres de haut, de couleur fuchsia fluo, acheté en promo chez Mr bricolage, surement que la couleur ne leur plaisait pas ? bon c’est vrai qu’elle est vraiment flashy !
				</p><p>
				Je commence la construction du robot découpant deux passages dans le sous pot de fleur fuchsia les roues motrices, je fixe en même temps la roulette mobile à l’avant. Je dois renforcer le fond trop mou avec des traverses récupérées dans des barres de coupe de massicot, fixées au fond par des vis. J’installe sur chaque roue un axe traversant avec un roulement à chaque extrémité et une poulie motrice, le tout est fixé sur les traverses avec des colliers de plomberie.
				</p><p>
				Pour construire le volume du robot, je fixe quatre tiges filetées dans chaque coin. Le premier étage est constitué de cornières en aluminium, des méplats renforcent la structure entre le fond et le premier étage. J’ai déporté l’avant et l’arrière du robot en prolongeant l’angle du sous pot jusqu’au premier étage, ce qui lui donne une forme rectangulaire pour les étages supérieur. Le plancher du premier étage est réalisé en contreplaqué, puis fixé aux tiges filetées grâce à quatre écrous. Le deuxième étage est constitué de contre-plaqué reposant sur deux cornières en aluminium, elles mem fixées sur les tiges filetées, il est articulé sur l’arrière du robot grâce à des charnières, pour d’accéder au premier étage.
				</p>
