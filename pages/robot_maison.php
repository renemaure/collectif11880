				<h3 class="blog-post-title">Septembre 2017</h3>	
				<p>
				Après trois mois à se maudire, je reprends enfin la construction du robot. Je dois racheter un sous pot de fleur et Il n’y avait plus de couleur fuchsia, je me rabats sur un rouge pompier. J’ai dû reprendre toute les découpes et les perçages, j’en ai profité pour alléger la structure des poutres, découpé la plateforme moteur, pour le passage des roues et des poulies moteur, et recouvert le tout de papier vinyle jaune. Après plusieurs heures de remontage, j’ai enfin fini la partie structure du robot. Je m’attaque ensuite à la construction du module batterie et lui trouve une place sur la plateforme moteur entre les deux axes des roues.
				</p><p>
				Grâce au projet robotique Cerda, un projet trouvé sur internet, j’ai pu calculer la vitesse et le nombre de mètres minutes que doit atteindre mon robot. Le moteur a un rapport de 180 sur 1, une vitesse de 80 tours par minute à la sortie de l’arbre, la roue du robot fait un diamètre de 90mm, ce qui donne un développement de 565,2 mm à chaque révolution, soit une vitesse de 45216 mm par minutes pour un moteur à 100% de sa capacité. Pour un fonctionnement à 50%, le robot parcourra 226 mètres à la minute.
				</p>
