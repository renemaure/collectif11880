					<h5>Les marges</h5>
					<p class="text-justify">
						Des classes de Bootstrap permettent de régler les marges extérieures (margin) et intérieures (padding) sous la forme : 
						<br>
						<p class="text-center"><code>[type de marge][coté][taille d’écran(en option)]-[taille]</code></p>
						<ul class="list-unstyled">
							<li>
							marge :	
							</li>
								<ul> 
									<li><code>m</code> pour <code> margin</code></li>
									<li><code>p</code> pour <code>  padding</code></li>
								</ul>
							<li>
							Coté : Si on ne présise pas le type de marge, elle s’appliquera à tous les côtés.
							</li>
								<ul> 
									<li><code>t</code> pour <code> haut</code></li>
									<li><code>b</code> pour <code>  bas</code></li>
									<li><code>r</code> pour <code> droite</code></li>
									<li><code>l</code> pour <code> gauche</code></li>
									<li><code>y</code> pour <code> haut & bas</code></li>
									<li><code>x</code> pour <code>  droit & gauche</code></li>
								</ul>
							<li>
							Tailles : N'oubiez pas le tiret avant!
							</li>
								<ul> 
									<li><code>0</code> pour supprime les marges.</li>
									<li><code>1</code> pour 0.25 rem.</li> 
									<li><code>2</code> pour 0.5 rem.</li>
									<li><code>3</code> pour 1 rem.</li>
									<li><code>4</code> pour 1.5 rem.</li>
									<li><code>5</code> pour  3 rem</li>
									<li><code>auto</code> uniquement pour <auto>margin</auto> defini un css à <code>margin : auto</code></li>
								</ul>		
						</ul>
					</p>