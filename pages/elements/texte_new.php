<h2 class="centrer italic coul-bord">Tous les ateliers pour la saison 2023 2024</h2>
<p class="justify">
Depuis janvier 2023, notre association collectif 11880, s’est installée définitivement au <span class="g coul-bord">1ter, place de la république à Migennes</span>, dans les locaux de la recyclerie Repère Echoppe 89.
	<br>
	Nous vous proposons pour cette rentrée, 3 ateliers, et plusieurs projets:
</p>	
<p>
Tous <span class="g coul-bord">les mercredis</span>, l’atelier mécanique, avec le projet de construire un vélo couché, à partir de composants de récupération et en utilisant uniquement un outillage de bricoleur.	
</p>
<p>Tous <span class="g coul-bord">les dimanches</span>, un atelier de programmation internet, permettant d’aborder le html, le css, le javascript, le php et les fichiers json, ouvert aussi bien aux novices, qu’aux bidouilleurs !
</p>
<p>
Et tous <span class="g coul-bord">les vendredis soir</span>, un atelier robotique, pour bidouiller les Arduino et les Raspberry, faire de la domotique, et ou de l’électronique.
</p>
<p>Nous avons aussi récupéré <span class="g coul-bord">une baie serveur</span>, que nous remontons et nous allons essayer de remettre en route, avis aux amateurs.
</p>
