<h2 class="centrer italic coul-bord">De bonne nouvelle pour 2023</h2>
<p class="justify">
	Depuis le début février, notre association s’installe définitivement au <span class="g coul-bord">1ter, place de la république à Migennes</span>, dans la recyclerie Repère Echoppe 89, et nous proposons 2 nouvelles activités :
	<br>
	La construction d’un vélo couché, à partir de composants de récupération de vélo et de tubes en acier, et en utilisant uniquement un outillage de bricoleur. 
<br>
	Un atelier de programmation internet, permettant d’aborder le html, le css, le javascript, mais aussi le php et les fichiers json, ouvert aussi bien aux novices, qu’aux bidouilleurs !
</p>
<p>
	<span class="g coul-bord">Le 5 avril 2023 à partir de 18H</span>, nous organisons notre premier atelier, vélo couché, pour rencontrer les personnes intéressées.
</p>
<p>
	<span class="g coul-bord">Le 16 avril 2023 à partir de 9H30</span>, (c’est un dimanche), nous lançons un atelier programmation internet (html, css, javascript).
</p>
<p>
	Nous tenons aussi deux permanences : <span class="g coul-bord">un dimanche sur deux, le matin de 9H à 12H, et le mercredi à partir de 18H !</span> n’hésitez pas à venir nous rencontrer.
</p>
