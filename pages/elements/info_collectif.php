Le Collectif 11880 est né d’artistes belges et français et des bidouilleurs du CMIT. En 2004, ce collectif participe à la biennale de Gant en Belgique.
<br>
Fort de ce succès, le collectif intègre le CMIT, avec l’ambition de faire entrer le numérique dans l’art et l’art dans le numérique pour aider les artistes et les associations à la création d’art électronique.

