					<h5>Container</h5>
					<p class="justify">
						Pour utiliser le système correctement le système de grilles de Bootstrap, il faut définir dans un élément html, une classe <code>container</code> ou <code>container-fluid</code>.
					</p>
					<dl>
						<dt>La classe <code>container</code></dt>
						<dd>permet un contenu responsive pour un type d’écran donné et procède des marges 						droite et gauche.
						</dd>
						
						<dt>La classe <code>container-fluid</code></dt>
						<dd> fait100% de la taille de l’écran et quel que soit le type d’écran.</dd>
						<dt>Attention ! </dt>
						<dd>Quand vous copiez des codes de sites internet, ne copiez pas le <code>div-container</code> de départ, j’ai déjà vu ça sur un site ami.</dd>
					</dl>
					<h5>La grille</h5>
					<p class="justify">
						Bootstrap utilise un système de grilles fait de 12 colonnes <code>classe col</code>, reparties en horizontal sur une ligne <code>classe row</code>. On peut grouper ou non les colonnes <code>col</code> entre elles mais sans jamais dépasser les 12 colonnes.
					</p>
					<p class="text-justify">
						Le truc génial, c’est que dans chaque colonne réée, on dispose à nouveau de 12 colonnes, ce qui permet de construire très vite un site internet complexe sans trop s’attarder au contenu. Très bien pour une association avec des membres légèrement distraits. N’en abusez pas non plus, car a chaque colonne crée Bootstrap rajoute des marges gauche et droites, ce qui peut finir par un bazar complet ! On connait !
					</p>