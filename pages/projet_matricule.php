    <h3 class="coul-bord">représentations du nombre 11880</h3>
    <p class="justify">
        La première représentation du matricule 11880, c’est fait au travers afficheur alphanumérique gris, constitué de cinq digits de sept segments rouges. Un circuit électronique autonome faisait clignoter inlassablement le nombre 11880.
         <img src="systeme/images/cadre01.png" class="ml-4 float-right"> 
        Deuxième représentation du nombre 11880, c’est fait à partir d’un afficheur gris de cinq segments monté au centre d’un cadre en aluminium anodisé, annoté de plusieurs slogans. Un circuit électronique complexe, fixé au dos du cadre assure l’animation du nombre 11880.
    <br>
        La troisième représentation du nombre 11880, c’est fait sous la forme d’un dessin informatique, représentant symboliquement un afficheur alphanumérique rouge de cinq segments, affichant le nombre 11880.
         <img src="systeme/images/ecran11880pt.png" class="mr-4 float-left"> 
         La quatrième représentation du nombre 1880 a été représenté sous la forme d’un programme informatique installé dans plusieurs ordinateurs diffusant en même temps la représentation symbolique d’un afficheur alphanumérique rouge de cinq segments diffusant une animation du nombre 11880.
   <br>
        La cinquième représentation du nombre 11880, a été représenté sous la forme d’un site internet diffusant une représentation symbolique d'un afficheur alphanumérique de cinq segments en diffusant une animation du nombre 11880. 
        </p>
     <h3 class="coul-bord">Les expositions</h3>
    <p>
        Ce nombre 11880, a été exposé pour la première fois à l'exposition En Marge à Metz en novembre 2003, sous la forme d’une image informatique imprimée sur un bristol et monté dans un cadre photo au format A4.
        <img src="systeme/images/longaville02pt.png" class="ml-4 float-right pb-4"> 
        Ce nombre 11880, fut ensuite exposé avec la galerie Artzoom à Longlaville, en septembre en 2005 au travers de huit ordinateurs synchronisés, diffusant le nombre 11880. Grace à cette exposition, le projet matricule rencontra un grand intérêt auprès du public comme de la presse. Un article fut publié dans l’Est Républicain sur la place de l’art électronique en France et en Europe.
        <br>
        Dans le même temps, plusieurs cardes Matricule, mini afficheurs et même une cassette VHS ont été dispersés dans la région parisienne au prés de différentes expositions ou de galeries, soit officiellement, soit anonymement, sorte de contre exposition.
    </p>
    <h3 class="coul-bord">Et dans le futur</h3>
    <p>
        Pour continuer à dé-incarner ce nombre 11880, Il pourra ensuite être représenté sous forme d’une structure lumineuse, sous forme d’une source vidéo, d’une ambiance sonore, ou peut-être en enseigne publicitaire.
        <br>
        Ainsi, fin 2020 un nouveau site internet a été relancé pour présenter le nombre 11880 au travers d’un afficheur alphanumérique, en réécrivant entièrement l’algorithme et en redessinant les digits de l’afficheur.

    </p>
