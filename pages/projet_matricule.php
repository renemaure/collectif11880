     <h1 class="center coul-bord espace4">Matricule 11880</h1>		
    <h3 class="coul-bord">Manifeste</h3>
	<p class="justify">
        Parce que toute une vie c’est réduite un jour à ce nombre - 11880 - pourtant une suite de chiffres banale et sans prétention, mais donnant pourtant un matricule personnel, devenant le seul identifiant totalement impersonnel. 
        <img src="systeme/images/afficheur_mini1.png" class="mr-4 float-left"> 
        Pour dé-incarner ce nombre – 11880 - il a fallu que je le transforme électroniquement, pour qu’il ne devienne plus qu’une éphémère pulsation d'électrons, un état électrique transitoire, une image électronique, un slogan, un logo, une marque, un concept. 
     </p>
    <h3 class="coul-bord">Les slogans</h3>
